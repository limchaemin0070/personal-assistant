import { createBrowserRouter, Navigate } from 'react-router-dom';
import { RegisterPage, LoginPage } from './pages';

export const router = createBrowserRouter([
    {
        path: '/',
        element: <Navigate to="/login" replace />,
    },
    {
        path: '/login',
        element: <LoginPage />,
    },
    {
        path: '/register',
        element: <RegisterPage />,
    },
]);
